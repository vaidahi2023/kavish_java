package operators;
//
//2.assignment operators
//--- +=,-=,*=,/=,%=
public class Assignmentoperators {

	public static void main(String[] args) {

			int a=10;
			a+=2;//a=a+2
			System.out.println(a);
			a-=1;
			System.out.println(a);
			a*=2;
			System.out.println(a);
			a/=2;
			System.out.println(a);
			a%=2;
			System.out.println(a);
	}
}
